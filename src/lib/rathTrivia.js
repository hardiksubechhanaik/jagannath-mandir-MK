function shuffle(arr) {
  const a = [...arr];
  for (let i = a.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [a[i], a[j]] = [a[j], a[i]];
  }
  return a;
}

function withShuffledOptions(question) {
  const tagged = question.options.map((text, index) => ({
    text,
    correct: index === question.answer,
  }));
  const shuffled = shuffle(tagged);
  return {
    q: question.q,
    options: shuffled.map((item) => item.text),
    answer: shuffled.findIndex((item) => item.correct),
  };
}

export function buildTriviaRound(size = TRIVIA_ROUND_SIZE) {
  const count = Math.min(size, TRIVIA_POOL.length);
  return shuffle(TRIVIA_POOL).slice(0, count).map(withShuffledOptions);
}
